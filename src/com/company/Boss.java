package com.company;

public class Boss {
    private int hitPoints;
    private int damage;
    private String typeDeffence;

    public int getHitPoints() {
        return hitPoints;
    }

    public void setHitPoints(int hitPoints) {
        this.hitPoints = hitPoints;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getTypeDeffence() {
        return typeDeffence;
    }

    public void setTypeDeffence(String typeDeffence) {
        this.typeDeffence = typeDeffence;
    }
}